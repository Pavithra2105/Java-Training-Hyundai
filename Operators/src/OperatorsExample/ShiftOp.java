package OperatorsExample;

public class ShiftOp {

	public static void main(String[] args) {
		
		//>> ,<< ,right shift ,left shift
		
		int a = 20;
		int b = a<<1;  //left shift gives the multiplication by 2 power the number mentioned
		System.out.println(a);
		System.out.println(b);
		
		int c = a>>1;  //left shift gives the division by 2 power the number mentioned
		System.out.println(c);
	}

}
